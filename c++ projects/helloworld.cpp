#include <iostream>

int main (){
    
    // declration 
    int x; 

    // assignmet 
    x = 9;  

    // declration and assignmet in one step.
    int y = 8;

    // how to execute 
    std::cout << y << '\n';
    std::cout << x << '\n';

    // how to sum 2 int
    int sum = y + x  << '\n'; 
    std::cout << sum << '\n' ;

    // double can store with point smt 
    double comma = 1.2;
    std::cout << comma << '\n';

    // char can store one char only 
    char grade = 'A';
    std:: cout << grade <<'\n';

    // boolen mean true or false 
    bool alive = false ;
    std:: cout << alive << '\n';

    // how to store string 
    std::string name = "Bro";
    std::cout << name << '\n';

    // anything that is const ins unchangeable
    const double PI = 3.14;
    const int WIDTH = 1920;
    const int HIGHT = 1080;
    std::cout << PI << '\n' << WIDTH << '\n' << HIGHT << '\n';


   
}